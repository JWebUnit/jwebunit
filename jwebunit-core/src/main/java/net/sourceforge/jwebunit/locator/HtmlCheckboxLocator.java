package net.sourceforge.jwebunit.locator;

/**
 * This is the interface for all HtmlCheckboxLocators.
 * A Html checkbox locator is a way to locate one &lt;input type="checkbox"&gt;
 * in an HTML page.
 * 
 * @author Julien Henry
 */
public class HtmlCheckboxLocator extends ClickableHtmlElementLocator {
    
    public HtmlCheckboxLocator() {
        super("input");
        addAttribut("type", "checkbox");
    }

}
